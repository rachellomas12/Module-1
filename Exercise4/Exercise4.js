function addition (x, y) {
    if (x == y) {
        return (x + y);
    }
    else
    {
        return (x + y);
    }
}
console.log(addition(5, 5));

function subtraction (x, y) {
    if (x == y) {
        return (x - y);
    }
    else
    {
        return (x - y);
    }
}
console.log(subtraction(6, 5));

function multiplication (x, y) {
    if (x == y) {
        return (x * y);
    }
    else
    {
        return (x * y);
    }
}
console.log(multiplication(6, 5));

